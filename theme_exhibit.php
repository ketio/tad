 						台北在城市發展途徑和由此產生的城市形態上，提供了一個和歐洲完全不同的城市經驗。Josef Bares 將在更廣闊的「閱讀」城市環境架構中，研究關於運動和記憶的話題。他會尋找連結著運動的場所，可以是橋樑、街道或者公共廣場。上班族通勤途中常見的典型交通路線和場景，會是研究的一個重要方面。這些地點的圖象和印象將會一起在混合媒體藝術作品的形式中起伏呈現。
