						<div>
						Josef Bares是一名捷克/德國血統的媒體藝術家，現於香港工作生活。他的藝術作品在各大洲展覽過，包括德國、奧地利、俄羅斯和美國等。符號系統領域是他的興趣所在。他用不同的方式去直觀地傳達正在發生意義的過程──意義的創造，尤其熱衷的是城市和語言、空間和語義之間的關係：我們如何「閱讀」我們的城市居住環境。建築在其「本質」中是共通的，但它在文化上卻大相徑庭──這點無異於語言。
